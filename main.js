import dayjs from 'https://cdn.skypack.dev/dayjs'
import customparse from 'https://cdn.skypack.dev/dayjs/plugin/customParseFormat'
import dayofyear from 'https://cdn.skypack.dev/dayjs/plugin/dayOfYear'

dayjs.extend(customparse)
dayjs.extend(dayofyear)

// format will be
// keys here will be years
// in those there are keys which are days of the year
// in those there are arrays of file names
const organized = {}

const dated = photos.map((a) => [a, dayjs(a, 'YYYYMMDD_HHmmss.jpg')])

dated.forEach(([photo, date]) => {
  const year = date.year()
  const day_of_year = date.dayOfYear()
  if (!(year in organized)) {
    organized[year] = {}
  }
  if (!(day_of_year in organized[year])) {
    organized[year][day_of_year] = []
  }
  organized[year][day_of_year].push([photo, date])
})

Object.entries(organized).forEach(([year, data]) => {
  const heading = document.createElement('h1')
  heading.innerText = year.toString()
  heading.id = year.toString()
  document.body.append(heading)
  Object.entries(data).forEach(([,photos]) => {
    const heading = document.createElement('h2')
    heading.innerText = photos[0][1].format('MMMM D')
    document.body.append(heading)
    photos.forEach(([photo]) => {
      const img = new Image()
      img.src = `photos/${photo}`
      document.body.append(img)
    })
  })
})
